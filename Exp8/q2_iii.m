clear all; 
clc;
close all;
t=-5000:5000; %100 
fm=188.5/(2*pi); %12.5 
x=cos(31.4*t)+2*cos(188.5*t)+0.5*cos(43.98*t); 
subplot(2,2,1); 
plot(t,x); 
xlabel('time in sec'); ylabel('x(t)'); 
title('continuous time signal');
fs1=1.6*fm; 
n=-12:12; 
x1=cos(31.4*2*pi*fs1*n)+2*cos(188.5*2*pi*fs1*n)+0.5*cos(43.98*2*pi*fs1*n); 
subplot(2,2,2); 
stem(n,x1); 
hold on subplot(2,2,2); 
plot(n,x1,':'); 
title('discrete time signal x(n) with fs< 2fm'); 
xlabel('n'); 
ylabel('x(n)');
fs2=2*fm; 
n1=-6:0.1:6; 
x2=cos(31.4*2*pi*fs2*n1)+2*cos(188.5*2*pi*fs2*n1)+0.5*cos(43.98*2*pi*fs2*n1); 
subplot(2,2,3); 
stem(n1,x2); 
hold on subplot(2,2,3); 
plot(n1,x2,':'); 
title('discrete time signal x(n) with fs=2fm'); 
xlabel('n'); 
ylabel('x(n)');n2=-21:21; 
fs3=8*fm; 
